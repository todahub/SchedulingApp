import type { Label, RuleDictionaryEntry } from "./types";

function entry(
  label: Exclude<Label, `target_${string}`>,
  canonical: string,
  variants: string[],
  options?: Pick<RuleDictionaryEntry, "score" | "meta" | "blocksInnerLabels">,
): RuleDictionaryEntry {
  return {
    label,
    canonical,
    variants,
    score: options?.score,
    meta: options?.meta,
    blocksInnerLabels: options?.blocksInnerLabels,
  };
}

